package main
import "fmt"

func main(){
	name := "Rizki Nur"
	fmt.Println(name)

	name = "Rizki Nur Sepriana"
	fmt.Println(name)

	// Multiple Variable
	var (
		firstName = "Rizki"
		middleName = "Nur"
		LastName = "Sepriana"
	)

	fmt.Println(firstName)
	fmt.Println(middleName)
	fmt.Println(LastName)
}